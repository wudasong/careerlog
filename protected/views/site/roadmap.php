<p>    <span>最后一个段落，废话一句都没有，直接给我觉得很有用的时间效率的</span><span>9</span><span>个方法</span><span>:</span></p><p>    <span><span>1.<span>&nbsp;&nbsp;&nbsp;&nbsp;</span></span></span>&nbsp;<strong><span>写下</span></strong><strong><span>Event log(</span></strong><strong><span>事件日志</span></strong><strong><span>)</span></strong><strong><span>。</span></strong><span>这个习惯我已经坚持了</span><span>7</span><span>年真的非常受用。有一本每天随身带的本子，每天用一句话记录今天最受感触的朋友的忠告、好书的好句、同事的很好的人生观点职场建议等等。这些触动你的话，不是每时每刻都遇得到的。如果经历了就忘记了。而记下来，就记得了。这个习惯，让每一笔好的经历，每一次有营养的对话，每遇到一个深刻的人，都历历在目。</span></p><p>    <span><span>2.<span>&nbsp;&nbsp;&nbsp;&nbsp;</span></span></span>&nbsp;<strong><span>不逢人诉说自己的计划。</span></strong><span>心理学上有过研究，在对人倾诉理想和计划的过程中，人的心理很容易当做是在进行它。所以，一旦确认自己的时间计划和自我革新，不要随便分享。</span></p><p>    <strong><span><span>3.<span>&nbsp;&nbsp;&nbsp;&nbsp;</span></span></span></strong>&nbsp;<strong><span>学会不含敌意的拒绝。</span></strong><span>对那些&ldquo;并非我们自己想而是不好意思拒绝&rdquo;的事项，学会合理的拒绝。这不仅给自己多了很多时间，而且要记得：<strong>一个时时刻刻都不会拒绝别人的人，也不会被人记得。</strong></span></p><p>    <span><span>4.<span>&nbsp;&nbsp;&nbsp;&nbsp;</span></span></span>&nbsp;<strong><span>时间开销记录。</span></strong><span>每天记录第二天要做的事情以及可能需要花去的时间。不是形式，而是记录在你看得到的地方，然后用</span><span>A\B\C</span><span>分类，表示重要程度。</span></p><p>    <span><span>5.<span>&nbsp;&nbsp;&nbsp;&nbsp;</span></span></span>&nbsp;<strong><span>&ldquo;五分钟定律&rdquo;。</span></strong><span>如果当天没有了时间，但是正在进行一项需要长期坚持的行为，比如跑步。那千万不要跳过去直接进行下一个事项，哪怕只抽</span><span>5</span><span>分钟，也把跑步这个行为经历过，它是坚持很好的办法。</span></p><p>    <span>6</span><span>．</span>&nbsp;<strong><span>巧用&ldquo;内部黄金时间&rdquo;和&ldquo;外部黄金时间&rdquo;。</span></strong><span>内部黄金时间，是指自己效率最高的时候，做最重要的事情。有的人是上午，有的人的晚上，自己会比较清楚自己的内部黄金时间是什么时候。而&ldquo;外部黄金时间&rdquo;是指周围的人、事对应的黄金时间。比如和老板请假最好的时间是她吃好饭心情太不错，哈哈，或者你要投简历打个陌生拜访电话最好的时间是在下午</span><span>6</span><span>点以后，这个时候秘书下班了，直接绕过她能找到大老板。</span></p><p>    <span>7.&nbsp;<span>&nbsp;</span></span><strong><span>让觉得自己内部节奏被打乱的时候，停下来一天。</span></strong><span>对，停下来一天，把自己所有没有解决的杂事：比如晒个杯子，换季衣服拿出来，很久没有开通的银行卡，缴个网费，这些不重要但必须花时间做掉的事情做掉。你会发现，心情好多了。</span></p><p>    <span>8.&nbsp;<strong><span>&nbsp;</span></strong></span><strong><span>清扫整理内心空间，从清理物理空间开始。</span></strong><span>写时间事件计划之前，用两个小时把自己房间（或者寝室）好好收拾一遍。我有一个很深刻的感受，在计划时间时，如果周围环境杂乱不堪，做下的计划基本都有气无力。这个真的非常管用，可以试试。</span></p><p>    <span>9.&nbsp;<strong><span>&nbsp;</span></strong></span><strong><span>付出没有明显效果是坚持计划的必经之路，不要为此放弃。</span></strong><span>这个时候是最容易放弃的。但这个和打超级玛丽是一样的，想要加血加命也要等到攒满一定量的金币。别想着吃了一颗金币，就能加到一条命。</span></p><p>    <span>&nbsp;</span></p><p>    <span>&nbsp;</span></p></div>